question = input("Enter a mechanical engineering topic: ")

if question == "mechanical engineering":
    print("Mechanical engineering deals with machines, design, and manufacturing.")

elif question == "stress":
    print("Stress is force applied per unit area.")

elif question == "strain":
    print("Strain is deformation divided by original length.")

elif question == "hooke law":
    print("Hooke's law says stress is proportional to strain.")

elif question == "casting":
    print("Casting is making parts by pouring molten metal into a mold.")

elif question == "cnc":
    print("CNC machining uses computers to control machines.")

else:
    print("Topic not found. Please try a basic mechanical term.")

